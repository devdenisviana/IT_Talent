public class Operadores_Math {
    public static void main(String[] args) {
        System.out.println("Adição => 5 + 2 = " + (5 + 2));
        System.out.println("Subtração => 5 - 2 = " + (5 - 2));
        System.out.println("Multiplicação => 5 * 2 = " + (5 * 2));
        System.out.println("Quociente da divisão => 5 / 2 = " + (5 / 2));
        System.out.println("Resto da divisão => 5 % 2 = "+ (5 % 2));
        System.out.println("Para exibir o quociente exato da divisão: ");
        float a = 5.0f;
        float b = 2.0f;
        System.out.println("O quociente da divisão de a / b = "+(a/b));

    }
}
